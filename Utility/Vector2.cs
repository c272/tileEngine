using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace easyCase.Utility
{
    /// <summary>
    /// A simple Vector2 for use in controls.
    /// </summary>
    public class Vector2
    {
        public float X;
        public float Y;

        public Vector2(float x = 0, float y = 0)
        {
            X = x;
            Y = y;
        }
    }
}
